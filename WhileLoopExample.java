/**
 * WhileLoopExample
 */
public class WhileLoopExample {

    public static void main(String[] args) {
        
        //declaration of the loop counter variable
        int num = 1;

        //test or condition
        while (num <= 5) {

            System.out.println("we are learning loops" + num);

            //update to the variable
            num++;
        }
    }
}